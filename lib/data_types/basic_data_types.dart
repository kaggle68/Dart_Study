// Dart의 기본 자료형

void main() {
  String name = 'jiwon';
  // name. <- String에 해당하는 기능 자동완성 가능

  int age = 25;
  // age. <- int에 해당하는 기능 자동완성 가능

  double money = 69.99;
  // money. <- double에 해당하는 기능 자동완성 가능

  bool alive = true;
  // alive. <- bool에 해당하는 기능 자동완성 가능

  // num은 int & double의 부모 클래스
  num x = 12;
  x = 1.2;

  print(name);
  print(age);
  print(money);
  print(alive);
  print(x);
}
